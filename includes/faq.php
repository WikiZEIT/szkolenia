function get_faq_for_page($page) {
    global $all_faq;
    $result = [];
    foreach ($all_faq as $item) {
        if (in_array($page, $item['pages'])) {
            $result[] = [
                'question' => $item['question'],
                'answer'   => $item['answer'],
            ];
        }
    }
    return $result;
}

function get_all_faq() {
    global $all_faq;
    return array_map(function($item) {
        return [
            'question' => $item['question'],
            'answer'   => $item['answer'],
        ];
    }, array_values($all_faq));
}
